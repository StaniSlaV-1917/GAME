import api from '../api/axios';

/**
 * Будит спящую Fly-машину: на бесплатном Pay-as-you-go без min_machines_running
 * бэк засыпает после ~5 минут простоя. Cold-start первого запроса = 5-15 сек.
 *
 * Решение: на критичных страницах (где юзер скоро сделает важный API-вызов)
 * вызываем warmupPing() в onMounted — пока юзер заполняет форму или скроллит,
 * машина успевает проснуться. К моменту реального запроса бэк уже тёплый.
 *
 * Стратегия запроса:
 * - не await'им — возвращаемся сразу, не блокируя рендер
 * - игнорируем любые ошибки (если бэк всё ещё спит и timeout — это норма,
 *   главное что мы его раскачиваем)
 * - используем существующий axios-инстанс (тот же baseURL, no-auth headers)
 *
 * Где вызывается:
 * - CartView (юзер скоро жмёт «Оформить»)
 * - GamePage (юзер скоро жмёт «В корзину» или «Купить»)
 * - LoginView/RegisterView (auth-flow требует свежего бэка)
 * - ProfileView (edit-формы → PUT /users/me)
 */
export function warmupPing() {
  // /up — Laravel health-check, лёгкий (без БД, без Vue), отдаёт 200 OK
  api.get('/up', { timeout: 4000 }).catch(() => {
    // Молча — это превентивный ping, не ошибка для юзера
  });
}
